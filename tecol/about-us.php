                    The project idea first started to form in 2010, as expert from SCA were involved in a project to detect a cartel. They started to search for a way to prove a cartel existence. FRS experts offered the solution of using the antitrust application of the Noble prize winning game theory – basically elaborate mathematical models. Unfortunately the price for preparing such models went way over the budget of SCA. 
The idea was kick-started in 2011 when SCA was once again involved in a project concerning cartel creation. It turned out that the EU had the necessary legal base but nobody had the means to use it. The idea for economy of scale was introduced.<br> 
After several discussions expert from FRS and SCA got convinced that a tool could be build that would offer the means of the European citizens to detect cartels and uphold Regulation 1/2003.<br>
SCA as the organization with considerable experience in the field, started consultations with possible partners for forming a consortium that would realize the project in the most cost-efficient way.<br>
Building of the consortium, consultations and preparation for this project took more than 6 months.<br>
The consortium was formed to facilitate maximum cost-efficiency and expertise to the project.<br>    
