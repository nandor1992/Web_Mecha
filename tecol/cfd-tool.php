                  <p>Coordination Failure Diagnostics is a model that analyses real market processes with the help of time pattern  analysis and investigates whether they operate  efficiently. This is based on the assumption that existing cartels cause failures in the  observed process patterns. The CFD cartel-audit elicits conclusions from these process  patterns in order to find hidden cartels.</p>
                    <p>In essence, the approach uses microeconomic market processes  which should be objectively observable. Good analogy is the process of gravity  – if you throw an apple in the air it should fall on the ground. If it does  not, there could be several explanations – the government could have put a net  to catch all the apples thrown (regulations) or there could be some time-space  anomaly (special conditions) or just somebody could have stolen the apple  (cartel). In economics the most popular example is the supply-demand-price  relationship if you know that the supply for one commodity has increased and  the demand has been unchanged then you could assume the price should fall. If  instead it rises there is a premise for reasonable suspicion that in an absence  of government regulation there is cartel or price fixing agreement in the  market segment. What mathematics and economics can tell you is that the  'balance of probabilities' is very heavily leaning in one directions. </p>
