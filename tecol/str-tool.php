                  <p><strong>The STR tool is developed mainly on Structural Approach elaborated in the paper of Paul Grout  "Structural Approaches to Cartel Detection".</strong></p><br>
<p>The theoretical  literature on cartels is primarily concerned  with the identification of the conditions that allow independent firms to implement jointly  determined outputs and prices without resorting to explicit agreements. For this purpose,  economists have borrowed from the theory of repeated games. The analogy with repeated games arises  because cooperation requires  single firms to depart from  their myopically optimal actions. In the absence  of legally binding  agreements, cooperation in the present may therefore only be sustained by the threat  of future punishment in the event  of a deviation. Hence, in economic theory firms can maintain collusive prices only if they can  credibly threaten  to punish deviations by aggressive competition  in the future. Clearly, this presupposes repeated interaction.</p>
<p>In other words in certain types of market structure sustainability  of collusion is much easier. For example if in your town just two people sell  ice cream, it is much easier for them to reach illicit agreement for higher  price than the ice cream sellers in London, which are several hundreds.</p>
<p>With the STR approach you  can establish if the industry characteristics that the literature has singled  out as   being  relevant when   assessing the   sustainability  of tacit   collusion within  a market are present.</p>
<p>The STR tool will help you gather information, establish if  different market structures enhance cartel formation, and it will help you  complete and send a report to the relevant law enforcing authorities if they do  not.</p>
